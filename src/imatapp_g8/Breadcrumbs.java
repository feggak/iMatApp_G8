    public void updateLabels(String one, String two, String three) {
        // Update FIRST set
        if (one == null) {
            first.setText("");
        } else {
            first.setText(one);
        }
        // Update SECOND set
        if (two == null) {
            second.setText("");
        }
        if (!two.equals("KEEP")) {
            second.setText("> " + two);
        } else {}
        // Update THIRD set
        if (three == null) {
            third.setText("");
        } else {
            third.setText("> " + three);
        }
    }

